extension UIColor {
    static let themeColor = UIColor(hexString: "#1BBED0")
    static let f9f9f9 = UIColor(hexString: "#f9f9f9")
    static let oceanBlue = UIColor(hexString: "#06B7FD")
    static let unselectedTabItemColor = UIColor(hexString: "#202A4C");
    static let selectedSegment = UIColor(hexString: "#333333");
    static let unselectedSegment = UIColor(hexString: "#828282");
    static let borderColor = UIColor(hexString: "#49B7ED");
    static let primaryColor = UIColor(hexString: "#FDA625");
    static let headerSecondaryColor = UIColor(hexString: "#544AED");
  
    var hexString: String {
        let cgColorInRGB = cgColor.converted(to: CGColorSpace(name: CGColorSpace.sRGB)!, intent: .defaultIntent, options: nil)!
        let colorRef = cgColorInRGB.components
        let r = colorRef?[0] ?? 0
        let g = colorRef?[1] ?? 0
        let b = ((colorRef?.count ?? 0) > 2 ? colorRef?[2] : g) ?? 0
        let a = cgColor.alpha

        var color = String(
            format: "#%02lX%02lX%02lX",
            lroundf(Float(r * 255)),
            lroundf(Float(g * 255)),
            lroundf(Float(b * 255))
        )

        if a < 1 {
            color += String(format: "%02lX", lroundf(Float(a * 255)))
        }

        return color
    }
} 
